    <h2 id="a">Change Font Size</h2>

    <p>Most modern web browsers let you increase or decrease the size of page text by zooming.</p>

    <h3>1) Keyboard shortcuts</h3>
    <ul>
        <li><strong>Windows / Linux:</strong> <kbd>Ctrl</kbd> + <kbd>+</kbd> (zoom in), <kbd>Ctrl</kbd> + <kbd>-</kbd> (zoom out), <kbd>Ctrl</kbd> + <kbd>0</kbd> (reset)</li>
        <li><strong>macOS:</strong> <kbd>⌘</kbd> + <kbd>+</kbd> (zoom in), <kbd>⌘</kbd> + <kbd>-</kbd> (zoom out), <kbd>⌘</kbd> + <kbd>0</kbd> (reset)</li>
    </ul>

    <h3>2) Using the browser menu</h3>
    <ol>
        <li>Click the browser menu (three dots or lines) in the top-right corner.</li>
        <li>Find the <strong>Zoom</strong> control.</li>
        <li>Increase or decrease the zoom level as needed.</li>
    </ol>

    <h3>3) Change the default font size</h3>
    <ol>
        <li>Open your browser’s <strong>Settings</strong> or <strong>Preferences</strong>.</li>
        <li>Go to <strong>Appearance</strong> or <strong>Fonts</strong>.</li>
        <li>Select your preferred default font size for all pages.</li>
    </ol>

    <details>
        <summary style="font-weight: bold;">Tips for mobile browsers</summary>
        <ul>
            <li><strong>Android (Chrome):</strong> Menu → <em>Settings</em> → <em>Accessibility</em> → adjust <em>Text scaling</em>.</li>
            <li><strong>iOS (Safari):</strong> Tap <em>aA</em> in the address bar → choose <em>A</em> to decrease or <em>A</em> to increase.</li>
        </ul>
    </details>
    
