    <h2 id="b">Access Keys</h2>
    Access keys are keyboard shortcut keys. Different areas of the web site can be accessed by users using this feature.
    Another key needs to be pressed at the same time with the access key
    (eg. in Internet Explorer ALT and the access key need to be pressed).
    </br>
    The following access keys are available:
    <ul>
        <li>h- HOME </li>
        <li>a- ABOUT US </li>
        <li>u- UNDERGRADUATE </li>
        <li>p- POSTGRADUATE </li>
        <li>g- REGISTRATION </li>
        <li>c- CONTACT US </li>
        <li>v- Privacy </li>
        <li>y- Accessibility</li>
        <li>s- Staff Admin</li>

    </ul>

