        <p>Al-Aqsa University (AQU) in Gaza is an independent Palestinian academic institute, according to the law of civil service and the law (11) of higher education of the year 1998. </p>

        <p>Al-Aqsa University was established as a teachers’ academy in 1955. Later, in 1991, the academy became a teachers’ college known as the governmental education college, and in 2001, it attained the status of a university.
